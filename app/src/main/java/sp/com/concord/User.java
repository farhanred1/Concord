package sp.com.concord;

public class User {
    public String name, email, passwd, gender, age, type;

    public User() {

    }

    public User(String name,String email,String passwd, String gender, String age, String type) {
        this.name = name;
        this.email = email;
        this.passwd = passwd;
        this.gender = gender;
        this.age = age;
        this.type = type;

    }
}
